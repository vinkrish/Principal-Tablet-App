package in.principal.sqlite;

public class School {
	private int schoolId;
	private int principalTeacherId;
	
	public int getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(int schoolId) {
		this.schoolId = schoolId;
	}
	public int getPrincipalTeacherId() {
		return principalTeacherId;
	}
	public void setPrincipalTeacherId(int principalTeacherId) {
		this.principalTeacherId = principalTeacherId;
	}
}
